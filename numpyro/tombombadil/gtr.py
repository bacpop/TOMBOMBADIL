    for i in range(61):
        # M = M.at[i, i].set(-jnp.dot(M[i, :], pimult[i, :]))
        M = M.at[i, i].set(-jnp.dot(jnp.take(M, index), jnp.take(pimult, index)))
        index += 61 # or use lax.slice?
    return M

